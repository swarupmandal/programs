package comparator;

public class BankBasicSettingModel {
	
	private Long bankBasicId;
	private String bankMid;
	private String description;

	public Long getBankBasicId() {
		return bankBasicId;
	}

	public void setBankBasicId(Long bankBasicId) {
		this.bankBasicId = bankBasicId;
	}

	public String getBankMid() {
		return bankMid;
	}

	public void setBankMid(String bankMid) {
		this.bankMid = bankMid;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

}
