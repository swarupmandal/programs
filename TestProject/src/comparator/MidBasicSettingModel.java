package comparator;

public class MidBasicSettingModel {

	private Long midBasicId;
	private String description;
	private String mid;

	public Long getMidBasicId() {
		return midBasicId;
	}

	public void setMidBasicId(Long midBasicId) {
		this.midBasicId = midBasicId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

}
